#include<vector>
using namespace std;

class Solution
{
public:
	vector<int> fallingSquares(vector<vector<int>>& positions)
	{
		int n = positions.size();
		vector<int> ans(n, 0);
		
		return ans;
	}
};

int main()
{
	vector<vector<int>>p = { {5,10},{1,7},{1,2},{9,3},{1,6} };
	Solution s;
	s.fallingSquares(p);
}